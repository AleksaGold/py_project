def summation(a, b):
    """
    Суммирует 2 числа
    :param a: первое число
    :param b: второе число
    :return: сумма двух чисел
    """
    sum_ = a + b
    return sum_


def subtraction(a, b):
    """
    Вычитает одно число из другого
    :param a: первое число
    :param b: второе число
    :return: разница двух чисел
    """
    subtract = a - b
    return subtract

